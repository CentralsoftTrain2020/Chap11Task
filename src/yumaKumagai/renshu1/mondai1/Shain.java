package yumaKumagai.renshu1.mondai1;

public class Shain {
	private String name;
	private int kihonkyu;

	public Shain(String name, int kihonkyu) {
		super();
		this.name = name;
		this.kihonkyu = kihonkyu;
	}

	public String toString() {
		return "Shain [name=" + name + ", kyuyo=" + kihonkyu + "]";
	}
}
