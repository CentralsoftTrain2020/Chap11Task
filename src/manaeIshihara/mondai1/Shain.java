package manaeIshihara.mondai1;

public class Shain {

	String name;
	int kihonkyu;
	
	public Shain(String name, int kihonkyu) {

		super();
		this.name = name;
		this.kihonkyu = kihonkyu;
	}
	public String toString() {
		return "Shain [name=" + name + ", kihonkyu=" + kihonkyu + "]";
	}	
}
