package yujiAwa.mondai1;

public class Shain {
	String name;
	int kyuyo;
	
	public Shain(String name, int kyuyo) {
		super();
		this.name = name;
		this.kyuyo = kyuyo;
	}

	@Override
	public String toString() {
		return "Shain [name=" + name + ", kyuyo=" + kyuyo + "]";
	}
	
	

}
